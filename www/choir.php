				<h1>
	Sutherland Shire Children’s Choir
</h1>
<p>
	Sutherland Shire Children’s Choir provides singers from yr 1 to end of secondary school with quality choral instruction, lead by highly qualified, experienced and enthusiastic teachers. There is a conductor and accompanist present at each rehearsal.
</p>
<p>
	The Sutherland Shire Children’s Choir programme is designed to develop each child’s natural singing voice. The choristers learn correct vocal technique and breathing. Renowned Kodaly methods are used to achieve aural and music reading skills. In an atmosphere of fun and encouragement, the singers gain the talent and confidence to perform. They develop self-esteem, confidence and social skills.
</p>
<h2>
	What Style of Music Do We Sing?
</h2>
<p>
	The choir’s members are exposed to a wide range of musical genres including popular, musical theatre, classics, world music, folk, Australiana and humorous selections.
</p>
<p>
	Repertoire selected is quality music that is developmentally appropriate and enjoyable.
</p>
<h2>
	Choir Groups
</h2>
	The Sutherland Shire Children’s Choir currently has 3 separate choirs, rehearsing weekly. Children are placed into the choir that we feel is most appropriate for their level of vocal, musical and personal maturity. It is our aim to make each child feel challenged but happy within their choral environment.
</p>
<p>
	<b>Allegro Choir:</b> Group of young singers ranging from yr 1 at school. With an emphasis on fun and quality repertoire, the Allegros make a beautiful sound for such young choristers. As they develop their confidence and love for singing in a disciplined and fun environment they are also learning the fundamentals of music literacy and aural perception.
</p>
<p>
	<b>Training Choir:</b> The self esteem of each singer is nurtured as they are encouraged to present their songs as enthusiastically and confidently as they can. Vocal technique, musical literacy and performance skills are further developed with an emphasis on part singing.
</p>
<p>
	<b>Performing Choir:</b> Provides an opportunity for committed and talented choristers to achieve an advanced level of choral and performance skills. The performing choir is a great way for children with a common interest in singing to join together in a vibrant, enjoyable and professional environment.
</p>
<p>
	<b>Teen Voices (New in 2012):</b> A group for young men and women aged 13-18 yrs. Individual development is promoted in an enjoyable environment with an emphasis on control and development of the changing male voice while exploring the beauty, flexibility and expressive power of young female voices. The ensemble develops further the vocal and performance skills established in the Performing Choir.
</p>
